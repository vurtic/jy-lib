def cuboid(fXLength, fYLength, fZLength, uName='cuboid1', lPosition=(0, 0, 0)):
    """Creates a cuboid NURBS curve with given lengths.

    Args:
        fXLength (float): length in the X direction
        fYLength (float): length in the Y direction
        fZLength (float): length in the Z direction
        uName (string, optional): name of the curve. Defaults to 'cuboid1'
        lPosition (triple, optional): center position for the curve. Defaults to the origin
    Returns:
        curve: the cuboid curve
    """
    fX = fXLength / 2.0
    fY = fYLength / 2.0
    fZ = fZLength / 2.0

    lCrvCVs = [
        (-1*fX, -1*fY, -1*fZ),
        (-1*fX, -1*fY, 1*fZ),
        (-1*fX, 1*fY, 1*fZ),
        (1*fX, 1*fY, 1*fZ),
        (1*fX, 1*fY, -1*fZ),
        (-1*fX, 1*fY, -1*fZ),
        (-1*fX, -1*fY, -1*fZ),
        (1*fX, -1*fY, -1*fZ),
        (1*fX, -1*fY, 1*fZ),
        (-1*fX, -1*fY, 1*fZ),
        (-1*fX, 1*fY, 1*fZ),
        (-1*fX, 1*fY, -1*fZ),
        (1*fX, 1*fY, -1*fZ),
        (1*fX, -1*fY, -1*fZ),
        (1*fX, -1*fY, 1*fZ),
        (1*fX, 1*fY, 1*fZ),
    ]
    crvResult = pm.curve(n=uName, d=1, p=[(0, 0, 0)]*16)
    crvResult.setCVs(lCrvCVs, 'world')
    pm.move(lPosition[0], lPosition[1], lPosition[2], crvResult, ws=True, a=True)
    pm.makeIdentity(crvResult, a=True, t=True, r=True, s=True, n=0, pn=True)
    return crvResult

def create(uType, uName=None, lPosition=(0, 0, 0)):
    """Creates a curve based on the provided type based on the data in the CURVEINFO dictionary.

    Args:
        uType (str): type of curve
        uName (str, optional): name of the curve. Defaults to the type of curve with a number
        lPosition (triple, optional): position of the curve to be created at
    Returns:
        curve: the curve
    """
    if uName is None:
        uName = '{}1'.format(uType)
    crvResult = pm.curve(n=uName, d=CURVEINFO[uType]['degree'], p=CURVEINFO[uType]['cvs'])
    pm.move(lPosition[0], lPosition[1], lPosition[2], crvResult, ws=True, a=True)
    pm.makeIdentity(crvResult, a=True, t=True, r=True, s=True, n=0, pn=True)
    return crvResult


